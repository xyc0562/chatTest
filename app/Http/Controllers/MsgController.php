	 * @return Response
	 */
	public function index()
	{
		return view('msg');
	}

    public function send()
    {
        $msg = Input::get('msg');
        // Push to ZMQ
        $context = new ZMQContext();
        $socket = $context->getSocket(ZMQ::SOCKET_PUSH, 'my pusher');
        $socket->connect("tcp://localhost:5555");

        $socket->send($msg);
    }
}